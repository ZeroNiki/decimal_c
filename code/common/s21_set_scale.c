#include "common.h"


/* Пример: в bits[3] = 0x00004000 (4)
 * 0xFF80FFFF = 11111111 10000000 11111111 11111111
 */

// scale = 1 (00000000 00000000 00000000 00000001)

/* 00000000 00000100 00000000 00000000 &
 * 11111111 10000000 11111111 11111111
 * 00000000 00000000 00000000 00000000
 * 00000000 00000000 00000000 00001000 &
 * 00000000 00000000 00000000 11111111
 * 00000000 00000000 00000000 00001000 << 16
 * 00000000 00000001 00000000 00000000 */

void s21_set_scale(s21_decimal *value, int scale) {
  value->bits[3] &= 0xFF80FFFF;
  value->bits[3] |= (scale & 0xFF) << 16;
}
